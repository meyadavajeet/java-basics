package com.technicalajeet.learnerlesson;

public class TA3DecisionMaking {
    public static void main(String[] args) {
//        Decision making using if
        String vehicles ="No vehicles";
        if(vehicles.equals("Bus")){
            System.out.println("Will go to office by Bus");
        }else if(vehicles.equals("Car")){
            System.out.println("Will go to office by Car");
        }else{
            System.out.println("Won't go to office");
        }

    }
}
